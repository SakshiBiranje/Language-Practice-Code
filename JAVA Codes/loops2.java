import java.util.*;
public class loops2 
{
    public static void main(String args[])
     {
        int n = 4;
        int m = 5;
        //outer loop
        
        for(int i=1;i<=n;i++) 
        {
            //inner loops
            for(int j=1;j<=m;j++)
            {
                System.out.print("*");
            }
            System.out.println();
        }
    }
       // System.out.println();
    }

